            <h4>Ashtanga Yoga</h4>
            <p>In Sanskrit Ashtanga is translated as "Eight Limb path." Ashtanga yoga involves a very physically demanding sequence of postures, so this style of yoga is definitely not for the beginner. It takes an experienced yogi to really love it. Ashtanga starts with five sun salutation A's and five sun salutation B's and then moves into a series of standing and floor postures. In Mysore, India, people gather to practice this form of yoga together at their own pace—if you see Mysore-led Ashtanga, it's expected of you to know the series. Vinyasa yoga stems from Ashtanga as the flowing style linking breath to movement.</p>
