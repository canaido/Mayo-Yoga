            <h4>Yoga Flow/Vinyasa Yoga</h4>
            <p>Vinyasa means "to place in a special way" and in this case yoga postures. Vinyasa is the most athletic yoga style. Vinyasa was adapted from Ashtanga yoga in the 1980s. In Vinyasa classes, the movement is coordinated with your breath and movement to flow from one pose to another. Many types of yoga can also be considered Vinyasa flows such as Ashtanga, power yoga, and prana. Vinyasa styles can vary depending on the teacher, and there can be many different types of poses in different sequences. I personally teach an alignment-based style of vinyasa and choreograph new flows every time, but I also like to hold some of the poses a bit longer after warming up.</p>
