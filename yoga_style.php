            <h4>Hatha Yoga</h4>
            <p>The Sanskrit term "hatha" is an umbrella term for all physical postures of yoga. In the West, hatha yoga simply refers to all the other styles of yoga (Ashtanga, Iyengar, etc.) that are grounded in a physical practice. However, there are other branches of yoga such as kriya, raja, and karma yoga that are separate from the physical-based yoga practice. The physical-based yoga is the most popular and has numerous styles. Hatha yoga classes are best for beginners since they are usually paced slower than other yoga styles. Hatha classes today are a classic approach to breathing and exercises. If you are brand-new to yoga, hatha yoga is a great entry point to the practice.</p>
